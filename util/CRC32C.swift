import Foundation

fileprivate let kMaskDelta: UInt32 = 0xA282EAD8
fileprivate let kCRC32Xor: UInt32 = 0xFFFFFFFF

public func ReadUint32LE(_ buffer: [UInt8]) -> UInt32 {
    return buffer.withUnsafeBufferPointer {
        DecodeFixed32($0.baseAddress!)
    }
}

public func ReadUint32LE(_ pointer: UnsafePointer<UInt8>) -> UInt32 {
    return DecodeFixed32(pointer)
}

public func RoundUp(_ pointer: Data, alignedNumber: Int, givenIndex: Int = 0) -> Int {
    precondition((alignedNumber & (alignedNumber - 1)) == 0, "alignedNumber = \(alignedNumber) should be power of 2")
    return (givenIndex + (alignedNumber - 1)) & ~(alignedNumber - 1)
}

public func RoundUp(alignedNumber: Int) -> Int {
    precondition((alignedNumber & (alignedNumber - 1)) == 0, "alignedNumber = \(alignedNumber) should be power of 2")
    return (alignedNumber - 1) & ~(alignedNumber - 1)
}

// Handmade CRC32C function.
public func AcceleratedCRC32C_woSupport(crc: UInt32, data: UnsafePointer<UInt8>, n: Int) -> UInt32 {
    var p = data
    let e = p.advanced(by: n)
    var l = crc ^ kCRC32Xor

    @inline(__always)
    func STEP1() {
        let c = Int((l & 0xFF) ^ UInt32(p.pointee))
        l = kByteExtensionTable[c] ^ (l >> 8)
        p = p.advanced(by: 1)
    }

    @inline(__always)
    func STEP4(c: inout UInt32, s: Int) {
        let word = ReadUint32LE(p.advanced(by: s * 4))
        c = word ^ kStrideExtensionTable3[Int(c & 0xFF)] ^ kStrideExtensionTable2[Int((c >> 8) & 0xFF)] ^ kStrideExtensionTable1[Int((c >> 16) & 0xFF)] ^ kStrideExtensionTable0[Int(c >> 24)]
    }

    @inline(__always)
    func STEP4W(w: UInt32) {
        var val = w ^ l
        for _ in 0 ..< 4 {
            val = ((val >> 8) ^ kByteExtensionTable[Int(val & 0xFF)])
        }
        l = val
    }

    let x = p.advanced(by: RoundUp(alignedNumber: 4))
    while p < x {
        STEP1()
    }
    if p.distance(to: p) >= 16 {
        var crc0 = ReadUint32LE(p) ^ l; p += 4
        var crc1 = ReadUint32LE(p); p += 4
        var crc2 = ReadUint32LE(p); p += 4
        var crc3 = ReadUint32LE(p); p += 4

        while p.distance(to: e) >= 16 {
            STEP4W(w: 0)
            STEP4W(w: 4)
            STEP4W(w: 8)
            STEP4W(w: 12)
            p += 16
        }

        while p.distance(to: e) >= 4 {
            STEP4(c: &crc0, s: 0)
            let tmp = crc0
            crc0 = crc1; crc1 = crc2; crc2 = crc3; crc3 = tmp
            p += 4
        }

        l = 0
        STEP4W(w: crc0)
        STEP4W(w: crc1)
        STEP4W(w: crc2)
        STEP4W(w: crc3)
    }

    while p != e {
        STEP1()
    }

    return l ^ kCRC32Xor
}

// Use MacOS internel CRC32C C function instead.
public func AcceleratedCRC32C_wSupport(crc: UInt32, data: UnsafePointer<UInt8>, n: Int) -> UInt32 {
    return crc32c_arm(crc, data, n)
}

public func AcceleratedCRC32C(_ crc: UInt32, _ buf: UnsafePointer<UInt8>, _ size: Int) -> UInt32 {
    let result: UInt32
    #if os(macOS)
        result = AcceleratedCRC32C_wSupport(crc: crc, data: buf, n: size)
    #else
        result = AcceleratedCRC32C_woSupport(crc: crc, data: buf, n: size)
    #endif
    return result
}

fileprivate let CanAccelerateCRC32C: Bool = {
    let kTestCRCBuffer = Array("TestCRCBuffer".utf8)
    let kBufSize: Int = kTestCRCBuffer.count - 1
    let kTestCRCValue: UInt32 = 0xDCBC59FA
    return AcceleratedCRC32C(0, kTestCRCBuffer, kBufSize) == kTestCRCValue
}()

public func Extend(_ crc: UInt32, _ data: UnsafePointer<UInt8>, _ n: Int) -> UInt32 {
    if CanAccelerateCRC32C {
        return AcceleratedCRC32C_wSupport(crc: crc, data: data, n: n)
    }
    return AcceleratedCRC32C_woSupport(crc: crc, data: data, n: n)
}

public func Value(_ data: UnsafePointer<UInt8>, _ n: Int) -> UInt32 {
    return Extend(0, data, n)
}

public func Mask(_ crc: UInt32) -> UInt32 {
    return ((crc >> 15) | (crc << 17)) + kMaskDelta
}

func Unmask(_ masked_crc: UInt32) -> UInt32 {
    let rot = masked_crc - kMaskDelta
    return ((rot >> 17) | (rot << 15))
}

